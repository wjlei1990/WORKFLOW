  double precision, dimension(NDIM) :: data, syn, syn_phydisp
  double precision :: t01, t02, t03, dt1, dt2, dt3
  double precision :: dist
  integer :: npt1, npt2, npt3

  integer :: npts
  double precision :: dt,t0
  integer :: nerr

  !copy
  t01=t01_in
  t02=t02_in
  dt1=dt1_in
  dt2=dt2_in
  npt1=npt1_in
  npt2=npt2_in

  nerr = 0

  !check
  if(max(npt1,npt2) > NDIM) then 
    print *, 'ERROR: Too many number of points in data or syn'
    nerr = 1
    return 
  endif
  npts = min(npt1, npt2)

  if(abs(dt1-dt2) > TOL) then
    print *, 'ERROR: check if dt match(data and syn)'
    nerr = 1
    return
  endif
  dt=dt1

  if(abs(t01-t02) > dt) then
    print *, 'Check if t0 match(data and syn)'
    nerr = 1
    return
  endif
  t0=t01

  data(1:npt1)=data_in(1:npt1)
  syn(1:npt2)=syn_in(1:npt2)

  dist=dist_in

  nerr=0

